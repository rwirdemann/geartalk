<p>Bei <strong>Wingfoil Gear Talk</strong> triffst Du die wahren Experten. Die Menschen, die das Material gefahren
    haben, für das Du dich interessierst. Die Menschen, die weder für Hersteller arbeiten, noch Material geschenkt
    bekommen oder anderweitig incentiviert werden.
</p>

<p>
    Die Idee: Du fährst ein bestimmtes Board, Wing, Foil und schreibst einen kurzen Report über Deine Erfahrungen mit
    dem Material. Zusammen mit Deinen Eckdaten, wie Gewicht, Fahrkönnen oder Lieblingsspot liefert Dein Bericht
    wertvolle Informationen für andere Wingsurfer, die sich für Dein jeweiliges Setup interessieren.
</p>

<p>
    Hast Du konkrete Fragen zu einem Report und dem darin beschriebenen Material, kannst Du Kontakt zur Autorin
    aufnehmen, sofern diese dies zulässt. Autorinnen entscheiden, ob Frage / Antwort-Kombinationen sinnvollen
    Ergänzungen zum Report sind und diesem zugehörig veröffentlicht werden.
</p>

<p>
    <strong>Was ist drin für Autorinnen:</strong> Spaß am Berichten. Spaß daran Expertise weiterzugeben. Spaß daran
    andere Wingsurfer kennenzulernen und sein Netzwerk zu erweitern.
</p>

<p>
   <strong>Was ist drin für Suchende:</strong>  Herstellerunabhängige, ungefärbte Reports aus dem richtigen Leben.
</p>